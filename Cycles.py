#Excamples of how to use cycles to go through a list
vegetables = ["Carrot", "Brocoli", "Corn"]
for vegetable in vegetables:
    print(vegetable)
    print(vegetable + " Pie")
print(vegetables)

#Example of using sycles to count and add
nums = [50, 90, 10]
total = 0
for num in nums:
    total += num
print(nums)
print(total)